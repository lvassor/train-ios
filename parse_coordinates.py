def extract_coordinates_from_path(path_string):
    """Extract all X,Y coordinate pairs from an SVG path string."""
    # Remove all path commands (M, L, C, Q, Z, etc.) and keep only numbers
    # Split by commands first
    coords = []

    # Pattern to match numbers (including negative and decimals)
    number_pattern = r'-?\d+\.?\d*'

    # Find all numbers in the path
    numbers = re.findall(number_pattern, path_string)

    # Group numbers into X,Y pairs
    for i in range(0, len(numbers), 2):
        if i + 1 < len(numbers):
            x = float(numbers[i])
            y = float(numbers[i + 1])
            coords.append((x, y))

    return coords

